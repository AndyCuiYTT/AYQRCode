#import "AYGenerateQRCode.h"

@implementation AYGenerateQRCode

+ (UIImage *)ay_generateQRCodeWithContent:(NSString *)content
                            codeImageSize:(CGFloat)width
                                     logo:(UIImage *)logo
                                logoFrame:(CGRect)logoFrame
                                      red:(CGFloat)red
                                    green:(CGFloat)green
                                     blue:(CGFloat)blue{
    UIImage *image = [self ay_generateQRCodeWithContent:content codeImageSize:width red:red green:green blue:blue];
    //有 logo 则绘制 logo
    if (logo != nil) {
        UIGraphicsBeginImageContext(image.size);
        [image drawInRect:CGRectMake(0, 0, image.size.width, image.size.height)];
        [logo drawInRect:logoFrame];
        UIImage *resultImage = UIGraphicsGetImageFromCurrentImageContext();
        UIGraphicsEndImageContext();
        return resultImage;
    }else{
        return image;
    }
    return nil;
}



/**
 最原始二维码

 @param content 二维码内容
 @return 二维码图片
 */
+ (CIImage *)ay_generateQRCodeWithContent:(NSString *)content{
    CIFilter *filter = [CIFilter filterWithName:@"CIQRCodeGenerator"];
    NSData *contentData = [content dataUsingEncoding:NSUTF8StringEncoding];
    [filter setValue:contentData forKey:@"inputMessage"];
    [filter setValue:@"H" forKey:@"inputCorrectionLevel"];
    CIImage *image = filter.outputImage;
    return image;
}


/**
 修改二维码尺寸

 @param content 二维码内容
 @param width 二维码尺寸
 @return 二维码图片
 */
+ (UIImage *)ay_generateQRCodeWithContent:(NSString *)content codeImageSize:(CGFloat)width{
    CIImage *image = [self ay_generateQRCodeWithContent:content];
    CGRect intergralRect = CGRectIntegral(image.extent);
    CGFloat scale = MIN(width / CGRectGetWidth(intergralRect), width / CGRectGetHeight(intergralRect));
    size_t wid = CGRectGetWidth(intergralRect) * scale;
    size_t height = CGRectGetHeight(intergralRect) * scale;
    CGColorSpaceRef colorSpaceRef = CGColorSpaceCreateDeviceGray();
    CGContextRef bitmapRef = CGBitmapContextCreate(nil, wid, height, 8, 0, colorSpaceRef, (CGBitmapInfo)kCGImageAlphaNone);
    CIContext *context = [CIContext contextWithOptions:nil];
    CGImageRef bitmapImage = [context createCGImage:image fromRect:intergralRect];
    CGContextSetInterpolationQuality(bitmapRef, kCGInterpolationNone);
    CGContextScaleCTM(bitmapRef, scale, scale);
    CGContextDrawImage(bitmapRef, intergralRect, bitmapImage);
    
    CGImageRef scaledImage = CGBitmapContextCreateImage(bitmapRef);
    CGContextRelease(bitmapRef);
    CGImageRelease(bitmapImage);
    return [UIImage imageWithCGImage:scaledImage];
}

//改变二维码颜色
+ (UIImage *)ay_generateQRCodeWithContent:(NSString *)content codeImageSize:(CGFloat)width red:(CGFloat)red green:(CGFloat)green blue:(CGFloat)blue{
    UIImage *image = [self ay_generateQRCodeWithContent:content codeImageSize:width];
    int imageWidth = image.size.width;
    int imageHeight = image.size.height;
    size_t bytesPerRow = imageWidth * 4;
    uint32_t *rgbImageBuf = (uint32_t *)malloc(bytesPerRow * imageHeight);
    CGColorSpaceRef colorSpaceRef = CGColorSpaceCreateDeviceRGB();
    CGContextRef context = CGBitmapContextCreate(rgbImageBuf, imageWidth, imageHeight, 8, bytesPerRow, colorSpaceRef, kCGBitmapByteOrder32Little|kCGImageAlphaNoneSkipLast);
    CGContextDrawImage(context, CGRectMake(0, 0, imageWidth, imageHeight), image.CGImage);
    //遍历像素, 改变像素点颜色
    int pixelNum = imageWidth * imageHeight;
    uint32_t *pCurPtr = rgbImageBuf;
    for (int i = 0; i<pixelNum; i++, pCurPtr++) {
        if ((*pCurPtr & 0xFFFFFF00) < 0x99999900) {
            uint8_t* ptr = (uint8_t*)pCurPtr;
            ptr[3] = red*255;
            ptr[2] = green*255;
            ptr[1] = blue*255;
        }else{
            uint8_t* ptr = (uint8_t*)pCurPtr;
            ptr[0] = 0;
        }
    }
    //取出图片
    CGDataProviderRef dataProvider = CGDataProviderCreateWithData(NULL, rgbImageBuf, bytesPerRow * imageHeight, ProviderReleaseData);
    CGImageRef imageRef = CGImageCreate(imageWidth, imageHeight, 8, 32, bytesPerRow, colorSpaceRef,
                                        kCGImageAlphaLast | kCGBitmapByteOrder32Little, dataProvider,
                                        NULL, true, kCGRenderingIntentDefault);
    CGDataProviderRelease(dataProvider);
    UIImage *resultImage = [UIImage imageWithCGImage:imageRef];
    CGImageRelease(imageRef);
    CGContextRelease(context);
    CGColorSpaceRelease(colorSpaceRef);
    
    return resultImage;
}

void ProviderReleaseData (void *info, const void *data, size_t size){
    free((void*)data);
}


